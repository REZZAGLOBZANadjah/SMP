        // delete supplier
        private void btnDel_Click(object sender, EventArgs e)
        {
            Toast toast = new Toast();
            Dialog dialog = new Dialog();
            id = Convert.ToInt32(tileView1.GetFocusedRowCellValue("Supp_Id"));
            if(id>0)
            {
                try
                {
                    var rs = MessageBox.Show("عملية حذف", "هل أنت متأكد من هذه العملية", MessageBoxButtons.YesNo);

                    if (rs == DialogResult.Yes)
                    {
                        tb_Supp = db.TB_SUPP.Where(x => x.Supp_Id == id).FirstOrDefault();
                        db.Entry(tb_Supp).State = EntityState.Deleted;
                        db.SaveChanges();
                        toast.txtCaption.Text = "تم حذف مورد";
                        toast.Show();
                        updateData();
                    }
                }
                catch
                {
                    dialog.txtCaption.Text = "لا يوجد مورد لحذفه ";
                    dialog.Width = this.Width;
                    dialog.Show();


                }
            }
            else
            {
                dialog.txtCaption.Text = "لا يوجد مورد لحذفه ";
                dialog.Width = this.Width;
                dialog.Show();
            }
            


        }
