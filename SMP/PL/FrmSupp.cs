        }

        private void btnSearch_Click(object sender, EventArgs e)
        {
            var _search = editSearch.Text;
            gridControlSupp.DataSource = db.TB_SUPP.Where(x => x.Supp_Name.Contains(_search)||
                                                          x.Supp_Phone.Contains(_search)||
                                                          x.Supp_Email.Contains(_search)).ToList();
        }
        // edit supplier
        private void btnEdit_Click(object sender, EventArgs e)
        {
            PL.FrmSuppAdd frmAddSup = new PL.FrmSuppAdd();
            Toast toast = new Toast();
            Dialog dialog = new Dialog();
            try
            {
                id = Convert.ToInt32(tileView1.GetFocusedRowCellValue("Supp_Id"));
                tb_Supp = db.TB_SUPP.Where(x => x.Supp_Id == id).FirstOrDefault();
                if (tb_Supp != null)
                {
                    frmAddSup.editNameSup.Text = tb_Supp.Supp_Name.ToString();
                    frmAddSup.editPhoneSupp.Text = tb_Supp.Supp_Phone.ToString();
                    frmAddSup.edtEmailSupp.Text = tb_Supp.Supp_Email.ToString();


                    methods.by = tb_Supp.Supp_Image;
                    frmAddSup.picCoverSupp.Image = Image.FromStream(methods.convertImage());
                    frmAddSup.id = id;
                    frmAddSup.btnAddSupp.Text = "تعديل";
                    frmAddSup.Show();
                    updateData();

                }
                else
                {
                    toast.txtCaption.Text = "لا يوجد مورد لتعديله ";
                    toast.Show();
                }
                
            }
            catch
            {
                toast.txtCaption.Text = "لا يوجد مورد لتعديله ";
                toast.Show();

            }




        }

       
