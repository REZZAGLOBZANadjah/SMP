                    frmAddPur.editSell.Text = tb_Pur.Pur_Sell.ToString();
                    frmAddPur.editQte.Text = tb_Pur.Pur_Qt.ToString();

                    frmAddPur.editTBuy.Text = tb_Pur.Pur_TBuy.ToString();
                    frmAddPur.editTSell.Text = tb_Pur.Pur_TSell.ToString();
                    frmAddPur.editTRev.Text = tb_Pur.Pur_TRev.ToString();
                    frmAddPur.id = id;
                    frmAddPur.btnAddPur.Text = " تعديل ";
