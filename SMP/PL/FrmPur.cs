        }

        private void btnSearch_Click(object sender, EventArgs e)
        {
            var _search = editSearch.Text;
            gridControlPur.DataSource = db.TB_Pur.Where(x => x.Pur_Name.Contains(_search)||
                                                          x.Pur_Type.Contains(_search)||
                                                          x.Pur_Cat.Contains(_search) || 
                                                          x.Pur_Supp.Contains(_search) ||
                                                          x.Pur_Det.Contains(_search)  ).ToList();
        }
        // edit supplier
        private void btnEdit_Click(object sender, EventArgs e)
        {
            PL.FrmPurAdd frmAddPur = new PL.FrmPurAdd();
            Toast toast = new Toast();
            Dialog dialog = new Dialog();
            try
            {
                id = Convert.ToInt32(gridView1.GetFocusedRowCellValue("Pur_Id"));
                tb_Pur = db.TB_Pur.Where(x => x.Pur_Id == id).FirstOrDefault();
                if (tb_Pur != null)
                {
                    frmAddPur.editNamePur.Text = tb_Pur.Pur_Name;
                    frmAddPur.editTypePur.Text = tb_Pur.Pur_Type;
                    frmAddPur.editCat.Text = tb_Pur.Pur_Cat;
                    frmAddPur.editSup.Text = tb_Pur.Pur_Supp;
                    frmAddPur.editPurDet.Text = tb_Pur.Pur_Det;

