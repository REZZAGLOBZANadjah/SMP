                    updateData();
                }
                else
                {
                    toast.txtCaption.Text = "لا يوجد عملية شراء لتعديلها ";
                    toast.Show();
                }
            }
            catch

            {
                toast.txtCaption.Text = "لا يوجد عملية شراء لتعديله ";
                toast.Show();
            }

        }

       
